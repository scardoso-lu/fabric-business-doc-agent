def _meta_block(doc_type: str, path: Path) -> str:
    return textwrap.dedent(f"""\
        | | |
        | --- | --- |
        | **Type** | {doc_type} |
        | **Source file** | `{path.name}` |
        | **Generated** | {datetime.now().strftime('%Y-%m-%d')} |
    """)


def _depends_on_text(activity: PipelineActivity) -> str:
    if not activity.depends_on:
        return ""
    parts = []
    for dep in activity.depends_on:
        conditions = dep.conditions
        if not conditions or conditions == ["Succeeded"]:
            label = DEPENDENCY_CONDITION_LABELS["Succeeded"]
        else:
            labels = [DEPENDENCY_CONDITION_LABELS.get(c, c) for c in conditions]
            label = " or ".join(labels)
        parts.append(f"after {dep.activity_name} ({label})")
    return "; ".join(parts)


def _resolve_dataflow(
    activity: PipelineActivity,
    dataflow_map: dict[str, ParsedDataflow],
) -> ParsedDataflow | None:
    if activity.activity_type not in DATAFLOW_ACTIVITY_TYPES:
        return None
    props = activity.type_properties
    candidates = [
        props.get("dataflowId", ""),
        props.get("dataflow", {}).get("referenceName", "") if isinstance(props.get("dataflow"), dict) else "",
        activity.name,
    ]
    for candidate in candidates:
        if not candidate:
            continue
        stem = Path(candidate).stem
        if stem in dataflow_map:
            return dataflow_map[stem]
        for key, df in dataflow_map.items():
            if key.lower() == stem.lower():
                return df
    return None


def _resolve_notebook(
    activity: PipelineActivity,
    notebook_map: dict[str, ParsedNotebook],
) -> ParsedNotebook | None:
    if activity.activity_type not in ("TridentNotebook", "Notebook"):
        return None
    props = activity.type_properties
    candidates = [
        props.get("notebookId", ""),
        props.get("notebook", {}).get("referenceName", "") if isinstance(props.get("notebook"), dict) else "",
        activity.notebook_path or "",
        activity.name,
    ]
    for candidate in candidates:
        if not candidate:
            continue
        stem = Path(candidate).stem
        if stem in notebook_map:
            return notebook_map[stem]
        for key, nb in notebook_map.items():
            if key.lower() == stem.lower():
                return nb
    return None
