def _lineage_content(
    pipeline: ParsedPipeline,
    notebook_map: dict[str, ParsedNotebook],
    dataflow_map: dict[str, ParsedDataflow] | None = None,
) -> str:
    """Collect all notebook and dataflow code from a pipeline for column lineage extraction."""
    dataflow_map = dataflow_map or {}
    ordered = pipeline.ordered_activities()
    parts: list[str] = [
        f"Pipeline: {pipeline.name}",
        f"Description: {pipeline.description or '(none)'}",
        "",
    ]
    has_artifacts = False
    for activity in ordered:
        nb = _resolve_notebook(activity, notebook_map)
        if nb:
            parts.append(f"--- Notebook: {nb.name} (activity: {activity.name}) ---")
            parts.append(nb.all_code[:3000])
            parts.append("")
            has_artifacts = True
        df = _resolve_dataflow(activity, dataflow_map)
        if df:
            parts.append(f"--- Dataflow: {df.name} (activity: {activity.name}) ---")
            parts.append(_MCODE_PREAMBLE + df.all_mcode[:3000])
            parts.append("")
            has_artifacts = True
    if not has_artifacts:
        for activity in ordered:
            if activity.description:
                parts.append(f"Activity '{activity.name}': {activity.description}")
    return "\n".join(parts)


def _lineage_content_notebook(notebook: ParsedNotebook) -> str:
    """Return notebook code for column lineage extraction."""
    parts = [
        f"Notebook: {notebook.name}",
        f"Description: {notebook.description or '(none)'}",
        "",
        notebook.all_code,
    ]
    return "\n".join(parts)


def _lineage_content_powerautomate(flow: ParsedPowerAutomateFlow) -> str:
    """Return a description of the flow for column lineage extraction.

    Power Automate flows move data between services rather than transforming
    database columns, so lineage is expressed as a service-to-service data
    transfer description rather than a column mapping.
    """
    parts = [
        f"Power Automate Flow: {flow.name}",
        f"Description: {flow.description or '(none)'}",
        f"Trigger: {flow.trigger_summary or '(none)'}",
        f"Connected services: {', '.join(flow.connections) or '(none)'}",
        "",
        "Actions (in declared order):",
    ]
    for action in flow.actions:
        conn_note = f" via {action.connection}" if action.connection else ""
        after_note = f" (runs after: {', '.join(action.run_after)})" if action.run_after else ""
        parts.append(f"  - {action.name} [{action.type}{conn_note}]{after_note}")
    return "\n".join(parts)


def _lineage_content_dataflow(dataflow: ParsedDataflow) -> str:
    """Return dataflow M-code for column lineage extraction."""
    parts = [
        f"Dataflow: {dataflow.name}",
        f"Description: {dataflow.description or '(none)'}",
        "",
        _MCODE_PREAMBLE,
        dataflow.all_mcode,
    ]
    return "\n".join(parts)


