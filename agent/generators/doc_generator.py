"""
Markdown documentation generator.

Every document — pipeline, notebook, or dataflow — produces the same five sections:

  ## Purpose
  ## Flow
  ## Business Goal
  ## Data Quality & Alerts
  ## Column Lineage

Each section is generated by a dedicated LLM call so context stays focused.
The content summary (_pipeline_content / _notebook_content / _dataflow_content) is
built once and reused across all five calls for the same document.
"""

